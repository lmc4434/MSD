void microDelay (uint16_t delay)
{
  __HAL_TIM_SET_COUNTER(&htim2, 0);
  while (__HAL_TIM_GET_COUNTER(&htim2) < delay);
}

void TIM2_Init(void)
{
    RCC -> APB1ENR1 |= RCC_APB1ENR1_TIM2EN;            // Enable TIM2 clock

    // TIM2 Initialization
    TIM2 -> PSC = 15;                                // PSC + 1
    TIM2 -> ARR = 0xFFFFFFF;                          // Reload value
    TIM2 -> CCMR1 &= ~(0xFFFFFFFF);                    // Clear capture / compare bits
    TIM2 -> CCMR1 |= 0x1;                            // Set CC1 to input, IC1 to TI1
    TIM2 -> CCER &= ~TIM_CCER_CC1P;                    // Capture rising edge
    TIM2 -> CCER &= ~TIM_CCER_CC1NP;                // Non-inverted capture
    TIM2 -> CCER |= TIM_CCER_CC1E;                    // Enable channel 1 capture / compare
    TIM2 -> EGR |= 0x1;								// Force timer update
    TIM2 -> DIER |= TIM_DIER_CC1IE;                    // Enable interrupt upon capture / compare event
    NVIC_EnableIRQ(TIM2_IRQn);
}


